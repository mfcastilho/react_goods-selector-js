import React, { useState } from 'react';
import 'bulma/css/bulma.css';
import './App.scss';

export const goods = [
  'Dumplings',
  'Carrot',
  'Eggs',
  'Ice cream',
  'Apple',
  'Bread',
  'Fish',
  'Honey',
  'Jam',
  'Garlic',
];

export const App = () => {
  const [selectedGood, setSelectedGood] = useState('Jam');

  const handleGoodClick = good => {
    setSelectedGood(good === selectedGood ? '' : good);
  };

  return (
    <main className="section container">
      {selectedGood ? (
        <h1 className="title is-flex is-align-items-center">
          {selectedGood} is selected
          <button
            onClick={() => setSelectedGood(``)}
            data-cy="ClearButton"
            type="button"
            className="delete ml-3"
          />
        </h1>
      ) : (
        <h1 className="title is-flex is-align-items-center">
          No goods selected
        </h1>
      )}
      <table className="table">
        <tbody>
          {goods.map(good => (
            <tr
              className={`${good === selectedGood ? 'has-background-success-light' : ''}`}
              key={good}
              data-cy="Good"
            >
              <td>
                <button
                  data-cy={`${good === selectedGood ? 'RemoveButton' : 'AddButton'}`}
                  type="button"
                  className={`button ${good === selectedGood ? 'is-info' : ''}`}
                  onClick={() => handleGoodClick(good)}
                >
                  {good === selectedGood ? '-' : '+'}
                </button>
              </td>

              <td data-cy="GoodTitle" className="is-vcentered">
                {good}
              </td>
            </tr>
          ))}
        </tbody>
      </table>
    </main>
  );
};
